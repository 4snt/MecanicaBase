package com.mycompany.mecanicabase.model.operacao;

/**
 * Enumeração que representa os tipos de elevadores disponíveis na oficina.
 */
public enum TipoElevador {
    /**
     * Elevador utilizado especificamente para serviços de alinhamento e
     * balanceamento.
     */
    ALINHAMENTO_E_BALANCEAMENTO,
    /**
     * Elevador do tipo geral, usado para diversos tipos de serviços mecânicos.
     */
    GERAL
}
